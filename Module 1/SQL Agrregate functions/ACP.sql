SELECT COUNT(STUDENT_ID) AS Student_Id
FROM STUDENTS;

SELECT AVG(AGE) AS Average_Age
FROM STUDENTS;

SELECT SUM(STUDENT_SCORE) AS Total_Marks
FROM STUDENTS;
