
CREATE TABLE IF NOT EXISTS STUDENTS (
    STUDENT_ID TEXT,
    STUDENT_NAME TEXT,
    AGE INTEGER,
    STUDENT_SCORE REAL
);

INSERT INTO STUDENTS(STUDENT_ID, STUDENT_NAME, AGE, STUDENT_SCORE) VALUES
